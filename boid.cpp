boid::boid(void)
{
	flock=NULL;
	nb_agents=0;
}

boid::boid(int a_size)
{
	int i=0;
	flock=new agent[a_size];
	for(i=0;i<a_size;i++)
	{
		flock[i]=agent();
	}
	nb_agents=a_size;
}

// ===========================================================================
//                                  Destructor
// ===========================================================================
boid::~boid(void)
{
	delete flock;
	flock=NULL;
	nb_agents=0;

}

