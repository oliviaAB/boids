boid::boid(void)
{
	flock=NULL;
	nb_agents=0;
}

boid::boid(int a_size)
{
	flock=new agent[a_size];
	/*
	int i=0;
	for(i=0;i<a_size;i++)
	{
		flock[i].print_coord();
	}
	*/
	nb_agents=a_size;
}

// ===========================================================================
//                                  Destructor
// ===========================================================================
boid::~boid(void)
{
	delete flock;
	flock=NULL;
	nb_agents=0;

}

// ===========================================================================
//                                 Public Methods
// ===========================================================================

agent* boid::get_flock(void) const 
{
	return flock;
}

int boid::get_nb_agents(void) const
{
	return nb_agents;
}


void boid::update(void)
{

	int i=0;
	for(i=0;i<nb_agents;i++)
	{
		flock[i].new_coord(flock, nb_agents, i);
		flock[i].print_coord();
	}
}

